// Bundle Mitr Regular (the on-label text face, docs/design-reference §4) as a base64 module.
// The SDK may not make third-party requests (Charter §10.2) — no Google Fonts at runtime — so the
// font ships inside a LAZY chunk (dynamic import in designer/font.ts; budget-exempt like HEIC).
// Source: ../treatink/web/public/fonts/Mitr-Regular.ttf (the store's own bundled face).
// Run manually; output is committed.
import { readFileSync, writeFileSync } from 'node:fs';

const ttf = readFileSync('../treatink/web/public/fonts/Mitr-Regular.ttf');
const b64 = ttf.toString('base64');
writeFileSync(
  'src/designer/font-data.ts',
  `// GENERATED by scripts/gen-font-data.mjs — Mitr Regular (store: web/public/fonts/Mitr-Regular.ttf).
// Loaded ONLY via dynamic import (its own lazy chunk). Do not edit by hand.
export const MITR_REGULAR_BASE64 =\n  '${b64}';\n`,
);
console.log(`font-data.ts: ${(ttf.length / 1024).toFixed(0)} KB ttf → base64 module`);
